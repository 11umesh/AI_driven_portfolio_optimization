def create_lstm_sequences(data, lookback=60):
    X, y = [], []
    for i in range(lookback, len(data)-1):
        X.append(data.iloc[i-lookback:i].values)
        y.append(data.iloc[i+1].values)
    return np.array(X), np.array(y)


